#include "libft.h"

void	ft_bzero(void *dst, size_t n)
{
	char	*br;

	br = (char *)dst;
	if (n != 0)
	{	
		while (n != 0)
		{
			*br = 0;
			br++;
			n--;
		}
	}
}
